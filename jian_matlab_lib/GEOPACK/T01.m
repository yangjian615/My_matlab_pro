function [F,FA,FS] = T01_FFS(A,A0,DA);
%       SUBROUTINE FFS(A,A0,DA,F,FA,FS)
%       IMPLICIT  REAL * 8  (A - H, O - Z)
SQ1=sqrt((A+A0)^2+DA^2);
SQ2=sqrt((A-A0)^2+DA^2);
FA=2.D0/(SQ1+SQ2);
F=FA*A;
FS=0.5D0*(SQ1+SQ2)/(SQ1*SQ2)*(1.D0-F*F);
% end of function FFS

