function [BX,BY,BZ] = T01_BIRK_SHL(A,PS,X_SC,X,Y,Z);
%         SUBROUTINE BIRK_SHL (A,PS,X_SC,X,Y,Z,BX,BY,BZ)
% C
%	 IMPLICIT  REAL * 8  (A - H, O - Z)
%         DIMENSION A(86)
% C
CPS=cos(PS);
SPS=sin(PS);

S3PS=2.D0*CPS;
% C
PST1=PS*A(85);
PST2=PS*A(86);

ST1=sin(PST1);
CT1=cos(PST1);
ST2=sin(PST2);
CT2=cos(PST2);

X1=X*CT1-Z*ST1;
Z1=X*ST1+Z*CT1;
X2=X*CT2-Z*ST2;
Z2=X*ST2+Z*CT2;
% C
L=0;
GX=0.D0;
GY=0.D0;
GZ=0.D0;
% C
for M=1:2,
    %         DO 1 M=1,2     %!    M=1 IS FOR THE 1ST SUM ("PERP." SYMMETRY)
    % C                          AND M=2 IS FOR THE SECOND SUM ("PARALL." SYMMETRY)
    for I=1:3,
        %             DO 2 I=1,3
        P=A(72+I);
        Q=A(78+I);
        CYPI=cos(Y/P);
        CYQI=cos(Y/Q);
        SYPI=sin(Y/P);
        SYQI=sin(Y/Q);
        % C
        for K=1:3,
            %                DO 3 K=1,3
            R=A(75+K);
            S=A(81+K);
            SZRK=sin(Z1/R);
            CZSK=cos(Z2/S);
            CZRK=cos(Z1/R);
            SZSK=sin(Z2/S);
            SQPR=sqrt(1.D0/P^2+1.D0/R^2);
            SQQS=sqrt(1.D0/Q^2+1.D0/S^2);
            EPR=exp(X1*SQPR);
            EQS=exp(X2*SQQS);
            % C
            for N=1:2,
                %                  DO 4 N=1,2  %! N=1 IS FOR THE FIRST PART OF EACH COEFFICIENT
                % C                                AND N=2 IS FOR THE SECOND ONE
                for NN = 1:2,
                    %                    DO 5 NN=1,2 %!   NN = 1,2 FURTHER SPLITS THE COEFFICIENTS INTO 2 PARTS,
                    % C                                         TO TAKE INTO ACCOUNT THE SCALE FACTOR DEPENDENCE
                    
                    if (M==1),
                        FX=-SQPR*EPR*CYPI*SZRK;
                        FY=EPR*SYPI*SZRK/P;
                        FZ=-EPR*CYPI*CZRK/R;
                        if (N==1) ,
                            if (NN==1) ,
                                HX=FX;
                                HY=FY;
                                HZ=FZ;
                            else
                                HX=FX*X_SC;
                                HY=FY*X_SC;
                                HZ=FZ*X_SC;
                            end
                        else
                            if (NN==1) ,
                                HX=FX*CPS;
                                HY=FY*CPS;
                                HZ=FZ*CPS;
                            else
                                HX=FX*CPS*X_SC;
                                HY=FY*CPS*X_SC;
                                HZ=FZ*CPS*X_SC;
                            end
                        end
                        
                    else                            %!   M==2
                        FX=-SPS*SQQS*EQS*CYQI*CZSK;
                        FY=SPS/Q*EQS*SYQI*CZSK;
                        FZ=SPS/S*EQS*CYQI*SZSK;
                        if (N==1) ,
                            if (NN==1) ,
                                HX=FX;
                                HY=FY;
                                HZ=FZ;
                            else
                                HX=FX*X_SC;
                                HY=FY*X_SC;
                                HZ=FZ*X_SC;
                            end
                        else
                            if (NN==1) ,
                                HX=FX*S3PS;
                                HY=FY*S3PS;
                                HZ=FZ*S3PS;
                            else
                                HX=FX*S3PS*X_SC;
                                HY=FY*S3PS*X_SC;
                                HZ=FZ*S3PS*X_SC;
                            end
                        end
                    end
                    L=L+1;
                    
                    if (M==1) ,
                        HXR=HX*CT1+HZ*ST1;
                        HZR=-HX*ST1+HZ*CT1;
                    else
                        HXR=HX*CT2+HZ*ST2;
                        HZR=-HX*ST2+HZ*CT2;
                    end
                    
                    GX=GX+HXR*A(L);
                    GY=GY+HY *A(L);
                    GZ=GZ+HZR*A(L);
                end %5
                
            end %  4   CONTINUE
        end %  3   CONTINUE
    end %  2   CONTINUE
end %  1   CONTINUE

BX=GX;
BY=GY;
BZ=GZ;

% end of function BIRK_SHL

